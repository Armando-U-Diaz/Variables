public class Variables {
   public static void main(String[] args) {
        //Variables
        int edad = 31;
        double precio = 300.4;
        boolean disponible = true; //Verdadero o falso
        char genero;
        genero = 'M'; //M-Masculino o F-femanino

        //acceder a las variables
        System.out.println("Edad " + edad);
        //Modificar el valor
        edad = 35;
        System.out.println("Edad " + edad);
        
        //Imprimir precio
        System.out.println("Precio " + precio);

        //Imprimir disponible
        System.out.println("Exisencia " + disponible);

        //imprmir genero
        System.out.println("Genero " + genero);

    }
}
